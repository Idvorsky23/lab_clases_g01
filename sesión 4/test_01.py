"""Diccionario en Python"""
"""Los nombres de las key(llaves) van a ir escritos siempre en inúscula y
sin tildes (por convección o por buenas práctica"""
var_1 = {"nombre": "Susana", "edad":24, "promedio": 14.9}
print("Mi diccionario tiene el siguiente contenido:",var_1)

"""Agreggamos elementos con un nuevo key a mi diccionario"""

var_1["distrito"] = "Villa El Salvador"
var_1["nombre"] = "Cesar"
var_1["habilitado"] = True

print(var_1)